# Step 1: Define a function called greet that takes a parameter 'name'
def greet(name):
    #""""This function prints a greeting message to the user."""
    print(f"Hello, {name}! Welcome to python practice.")

# Step 2: Ask the user to enter their name
user_name = input("Enter your name: ")

# Step 3: Call the greet function with the user's input
greet(user_name)